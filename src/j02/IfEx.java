package j02;

/*
  제어문
  - 조건문
  	if
  	if ~ else
  	if ~ elif ~ else
  	switch ~ case
  	
  - 반복문
  	for
  	while
  	do ~ while
 */

public class IfEx {
	public static void main (String[] args) {
		int a = Integer.parseInt(args[2]); // 3 5 7
		if (a >= 5) {
			System.out.println("Greater or Equal");
		} else {
			System.out.println("Smaller");
		}
		
		
		
		
		
	}
}
